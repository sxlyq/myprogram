#include "location.h"

Location::Location(int r, int c)
{
    row = r;
    col = c;
}

bool Location::operator==(const Location & loc)
{
    return ((row==loc.getRow())&&(col==loc.getCol()));
}
int Location::getRow() const
{
    return row;
}
int Location::getCol() const
{
    return col;
}
void Location::setRow(int value)
{
    row = value;
}
void Location::setCol(int value)
{
    col = value;
}




